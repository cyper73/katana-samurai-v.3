class LanguageManager:
    def __init__(self, config_file="katana_config.json"):
        self.config_file = config_file
        self.current_lang = "en"  # Default
        self.load_config()
        
    def load_config(self):
        try:
            if os.path.exists(self.config_file):
                with open(self.config_file, 'r', encoding='utf-8') as f:
                    config = json.load(f)
                    self.current_lang = config.get("language", "it")
        except Exception as e:
            print(f"Error loading config: {e}")
            
    def save_config(self):
        try:
            config = {}
            if os.path.exists(self.config_file):
                with open(self.config_file, 'r', encoding='utf-8') as f:
                    config = json.load(f)
            
            config["language"] = self.current_lang
            
            with open(self.config_file, 'w', encoding='utf-8') as f:
                json.dump(config, f, indent=4)
        except Exception as e:
            print(f"Error saving config: {e}")
            
    def set_language(self, lang):
        if lang in TRANSLATIONS:
            self.current_lang = lang
            self.save_config()
            
    def get_text(self, key, **kwargs):
        lang_dict = TRANSLATIONS.get(self.current_lang, TRANSLATIONS["it"])
        text = lang_dict.get(key, key)
        if kwargs:
            try:
                return text.format(**kwargs)
            except KeyError:
                return text
        return text

